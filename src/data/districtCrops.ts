export function getRecommendedCrops(state: string, district: string): string[] {
  const info = DISTRICT_CROPS[state];
  if (!info) return [];
  if (district && info.districts[district]) {
    return info.districts[district];
  }
  return info.default;
}

export function getDistricts(state: string): string[] {
  const info = DISTRICT_CROPS[state];
  if (!info) return [];
  return Object.keys(info.districts).sort();
}
